# Definisi string
kalimat = "Halo, selamat datang di dunia Python!"

# Mengambil sebagian dari string
sub_kalimat = kalimat[6:20]

# Mengubah menjadi huruf besar
kalimat_besar = kalimat.upper()

# Menghitung jumlah kemunculan huruf 'a'
jumlah_a = kalimat.count('a')

print("Kalimat asli:", kalimat)
print("Sub kalimat:", sub_kalimat)
print("Kalimat besar:", kalimat_besar)
print("Jumlah huruf 'a':", jumlah_a)