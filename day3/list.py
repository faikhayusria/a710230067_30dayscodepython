# Definisi list
angka = [1, 2, 3, 4, 5]

# Menambahkan elemen ke dalam list
angka.append(6)

# Menghapus elemen dari list
angka.remove(3)

# Mengakses elemen dari list
elemen_pertama = angka[0]

# Mengurutkan list
angka.sort()

print("List setelah penambahan dan penghapusan:", angka)
print("Elemen pertama dalam list:", elemen_pertama)